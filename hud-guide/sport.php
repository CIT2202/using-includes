<h1>Sport in Huddersfield</h1>
<p>Football and rugby league are the main spectator sports in Huddersfield.
 Its professional football team, Huddersfield Town F.C. play in the Championship. In 1926, the club became the first in England to win three successive league titles, a feat only three other clubs have matched.The town was the birthplace of rugby league, and is home to the Huddersfield Giants who play in the Super League, the top division in Europe. The town is home to Huddersfield Underbank Rangers rugby league club, who play in the Rugby League Conference. Huddersfield Rugby Union Football Club play in the National Division Three North and Huddersfield Rams Aussie Rules club. The main sporting arena is John Smith's Stadium home to both the football team and rugby league side.
